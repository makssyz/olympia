public class Athlete {

    private final int id;
    private String name;
    private int birthyear;
    private String gender;
    private int height;
    private int weight;

    Athlete(int id, String name, int birthyear, String gender, int height, int weight) {
        this.id = id;
        this.name = name;
        this.birthyear = birthyear;
        this.gender = gender;
        this.height = height;
        this.weight = weight;
    }
}
